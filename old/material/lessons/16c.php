	<p>
		<span class="navword">Kaltxì!</span>
		<!-- <img src="img/smiley.gif"/> --> Long time no post. This time I&#39;d like to talk about how to say someone makes someone do something.
	</p>
	<p>
		In English, we have to use two words to get this idea across: <span class="gold-text">make</span> and the <span class="cyan-text">verb</span>. For example:
	</p>
	<blockquote>
		<em>
			<span class="blue-text">The man</span> <span class="gold-text">makes</span> <span class="green-text">his son</span> <span class="cyan-text">eat</span> <span class="red-text">the food</span>.
		</em>
	</blockquote>
	<p>
		In Na&#39;vi, there is just a little thing — &lt;<span class="gold-text">eyk</span>&gt; — that you put inside the <span class="cyan-text">verb</span>. The above example sentence would work like this:
	</p>
	<blockquote>
		<strong>
			<span class="blue-text">Tutanìl</span> <span class="green-text">sneyä &#39;itanur</span> <span class="cyan-text">
				y<span class="gold-text">eyk</span>om
			</span> <span class="red-text">syuveti</span>.
		</strong>
	</blockquote>
	<p>
		Notice the endings; this is key: <strong>
			-<span class="blue-text">l</span> <span class="gold-text">makes</span> -<span class="green-text">r</span> <span class="cyan-text">do</span> -<span class="red-text">t</span>
		</strong>. — a little rhyme I just make up about the endings used when &lt;<span class="gold-text">eyk</span>&gt; is used in a verb that can be enacted upon something. Also note that &lt;<span class="gold-text">eyk</span>&gt; can also be interpreted as the verb <span class="gold-text">
			<em>cause</em>
		</span>. Here are some more examples:
	</p>
	<div class="endpostpreview"></div>
	<blockquote>
		<span class="navword">Eytukanìl Neytiriru yerikit teykaron.</span>
		<br />
		<em>Eytukan makes Neytiri hunt hexapede.</em>
		<br />
		<br />
		<span class="navword">Oel tseyke&#39;a ngaru poti </span>
		<br />
		<em>I cause you to see her.</em>
		<br />
		<br />
		<span class="navword">Pol veyke&#39;kì oeru oeyä tsmukanti</span>
		<br />
		<em>She causes me to hate my brother</em>
		<br />
	</blockquote>
	<p>All the above verbs have things that are acted upon, like yerik, her, and my brother. But what about those times you make someone/something do something but they aren&#39;t acting upon anything? Maybe something like this:</p>
	<blockquote>
		<em>
			<span class="blue-text">Wind</span> <span class="gold-text">makes</span> <span class="red-text">me</span> <span class="cyan-text">fall</span>.
		</em>
	</blockquote>
	<p>That works like this:</p>
	<blockquote>
		<strong>
			<span class="cyan-text">
				Z<span class="gold-text">eyk</span>up
			</span> <span class="red-text">oeti</span> <span class="blue-text">hufwel</span>.
		</strong>
	</blockquote>
	<p>
		This time, <strong>
			-<span class="blue-text">l</span> <span class="gold-text">makes</span> -<span class="red-text">t</span> <span class="cyan-text">do</span>
		</strong>. Here are some examples:
	</p>
	<blockquote>
		<span class="navword">Fula nga zaya&#39;u oeti lrrtok seyki!</span>
		<br />
		<em>this thing which is you will come makes me smile!</em>
		<br />
		<br />
		<span class="navword">Sa&#39;nokìl oeti keykä.</span>
		<br />
		<em>Mother makes me go.</em>
		<br />
		<br />
		<span class="navword">Palulukanìl oeti txopu seyki</span>
		<br />
		<em>Thanators cause me to do fear. (Thanator scare me)</em>
		<br />
	</blockquote>
	<p>
		Hopefully, this has been helpful. Feel free to ask questions and/or post more of these sentences in the comments below. <!-- <img src="img/cheesy.gif"/> --> Kiyevame.
	</p>
