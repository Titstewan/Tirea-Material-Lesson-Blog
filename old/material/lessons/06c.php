	<h3 id="notes">Some notes</h3>
	<p>
		As you see, In these numbered examples, the words in green are used as <i>separate words</i> <span class="green-text">before</span> <span class="blue-text">the thing</span> they work with. <img src="img/grin.gif"/>
		If it&#39;s working with a single <span class="blue-text">word</span>, it may go onto the end of the <span class="blue-text">word</span><span class="green-text">likethis</span>. So <i>separate before, attached after</i>. All adp. words work like that. The green Na&#39;vi words in the numbered examples make the first letter change thing happen so watch out! <img src="img/wink.gif"/>
