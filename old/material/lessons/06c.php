	<h2 id="1">This and That</h2>
	<p>
		So, to get started... This and that. How many times have you wanted to say something in Na&#39;vi like <i>This tree is huge</i> and <i>That plant is poisonous</i>, etc? Well, it&#39;s super easy to do this. We have <span class="navword">fì</span>- and <span class="navword">tsa</span>- which go onto the beginning of the word.
