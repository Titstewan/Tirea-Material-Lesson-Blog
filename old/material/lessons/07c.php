	<h2 id="1">Endings: -ru, -l, -ti</h2>
	<p>
		We&#39;ve talked about <span class="blue-text">-ìl</span>, <span class="blue-text">-l</span> and <span class="red-text">-ti</span>, <span class="red-text">-it</span>, <span class="red-text">-t</span>. We&#39;ve also already talked <em>a little bit</em> about using <span class="turquoise-text">-ur</span>, <span class="turquoise-text">-ru</span>, <span class="turquoise-text">-r</span> before, but in this post I want to talk more about it. I also wanna talk about how to use <span class="purple-text">-yä</span>, <span class="purple-text">-ä</span>. Ok so let&#39;s get started with some examples. <img src="img/smiley.gif"/>
	</p>
	<p>
		<u>Examples</u>:
	</p>
	<ol class="lindent">
		<li>
			<strong>
				Oe <span class="green-text">yomtìng</span> <span class="orange-text">ikran</span><span class="turquoise-text">ur</span>.
			</strong>
			<br />
			I <span class="green-text">feed</span> <span class="turquoise-text">to</span> <span class="orange-text">the ikran</span>.
			<br />
			<em>I feed the ikran.</em>
		</li>
	</ol>
	<p>
		As you see in this example, <strong>
			<span class="green-text">yomtìng</span>
		</strong> is a special action where the person feeding (giving the food) stays as it is, and the one being fed gets <span class="orange-text">-ur</span>, <span class="orange-text">-ru</span>, <span class="orange-text">-r</span> on the end.
