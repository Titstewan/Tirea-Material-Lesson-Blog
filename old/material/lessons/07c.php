	<h2 id="2">Those Crazy "____ si" Words</h2>
	<p>
		Now. I&#39;ll bet most of you if not all have seen the Na&#39;vi word <strong>si</strong>. I know that the dictionary definiton is <em>crazy</em> and scary. It&#39;s actually simple. <img src="img/smiley.gif"/> Here&#39;s how it works:
	</p>
	<div id="endpostpreview"></div>
	<p>
		<u>Examples</u>:
	</p>
	<ol start="6" class="lindent">
		<li>
			<strong>
				<span class="orange-text">Nga</span><span class="turquoise-text">ru</span> <span class="green-text">irayo si</span> oe.
			</strong>
			<br />
			I <span class="green-text">do thanks</span> <span class="turquoise-text">to</span> <span class="orange-text">you</span>.<br />
			<em>I thank you.</em>
		</li>
		<li>
			<strong>
				<span class="orange-text">Oe</span><span class="turquoise-text">ru</span> <span class="green-text">srung si</span> nga.
			</strong>
			<br />
			You <span class="green-text">do assistance</span> <span class="turquoise-text">to</span> <span class="orange-text">me</span><br />
			<em>you help me.</em>
		</li>
		<li>
			<strong>
				Po <span class="orange-text">awnga</span><span class="turquoise-text">ru</span> <span class="green-text">kavuk si</span>.
			</strong>
			<br />
			He <span class="green-text">does betrayal</span> <span class="turquoise-text">to</span> <span class="orange-text">us</span>.<br />
			<em>He betrays us.</em>
		</li>
		<li>
			<strong>
				Tsa&#39;u <span class="purple-text">oeyä</span> <span class="orange-text">eltu</span><span class="turquoise-text">r</span> <span class="green-text">tìtxen si</span>.
			</strong>
			<br />
			That <span class="green-text">does awakening</span> <span class="turquoise-text">to</span> <span class="purple-text">my</span> <span class="orange-text">brain</span>.<br />
			That awakens my brain.<br />
			<em>That&#39;s interesting.</em>
		</li>
	</ol>
	<p>
		This one is a common phrase. To say something is interesting in Na&#39;vi, you say it "wakes up the brain". <img src="img/grin.gif"/>
		<strong>eltur tìtxen si, kefyak?</strong> (<em>Interesting, isn&#39;t it?</em>)
	</p>
	<ol start="10" class="lindent">
		<li>
			<strong>
				Oe <span class="green-text">kìte&#39;e si</span> <span class="orange-text">Omatikaya</span><span class="turquoise-text">ru</span>.
			</strong>
			<br />
			I <span class="green-text">do service</span> <span class="turquoise-text">to</span> <span class="orange-text">the Omatikaya</span>.<br />
			<em>I serve the Omatikaya clan.</em>
		</li>
	</ol>
	<p>See the pattern?</p>
	<ul>
		<li>
			The word <strong>si</strong> <em>sort of</em> means "do"
		</li>
		<li>
			<strong>si</strong> almost never stands alone. It&#39;s usually a word then si.
		</li>
		<li>
			You can&#39;t just use it with any word. Luckily, all the "___ si" words are all listed in the dictionary. <img src="img/wink.gif"/>
		</li>
		<li>
			All the ones above use <span class="turquoise-text">-ur</span>, <span class="turquoise-text">-ru</span> on the end of the one receiving the action.
		</li>
		<li>The one doing the action just stays as is.</li>
	</ul>
	<h2 id="3">Ending: -ä</h2>
	<p>
		And now, a little about the <span class="purple-text">-yä</span>, <span class="purple-text">-ä</span>. You may have noticed this bit on the end of words in examples <strong>4</strong> and <strong>9</strong>. It&#39;s really simple:
	</p>
	<ul>
		<li>
			<span class="purple-text">-yä</span> and <span class="purple-text">-ä</span> are the same. There are two of them so there is a choice to use which one flows the best.
			<ul>
				<li>
					Use <span class="purple-text">-yä</span> on words that end with any of these: a ä e i ì.
				</li>
				<li>
					Use <span class="purple-text">-ä</span> on words that end with anything else.
				</li>
			</ul>
		</li>
		<li>
			These are put on the word which is owning the other. <span class="purple-text">-yä</span> and <span class="purple-text">-ä</span> are very similar to the English <span class="purple-text">
				<em>&#39;s</em>
			</span> or <span class="purple-text">
				<em>of the</em>
			</span>.
		</li>
		<li>
			<span class="purple-text">yä</span> can be put onto words like <strong>oe</strong> (<em>I</em>), <strong>nga</strong> (<em>you</em>), <strong>po</strong> (<em>s/he</em>), etc. to change the meaning to <em>my</em>, <em>your</em>, <em>his/her</em>, etc. But watch out! When you do this, these kinds of words change a bit so that they end in "<span class="purple-text">-eyä</span>" instead of *"<span class="purple-text">ayä</span>" or *"<span class="purple-text">oyä</span>"
		</li>
	</ul>
	<p>
		<u>Examples</u>:
	</p>
