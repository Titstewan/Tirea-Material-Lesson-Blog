	<p>
		<span class="navword">Kaltxì</span>! And welcome to Lesson 4 of Learn Na&#39;vi Grammar The Easy Way.
	</p>
	<p>This time we talk about describing stuff.</p>
	<h2 id="1">
		Describing with <span class="navword">lu</span>
	</h2>
	<p>
		In Na&#39;vi, We describe what something is with <span class="navword">lu</span>. In this case, it&#39;s like an "=" sign. Some examples:
	</p>
	<ol class="lindent">
		<li>
			<span class="navword">Eyktan lu txantslusam.</span>
			<br />
			<em>The leader is wise.</em>
		</li>
		<li>
			<span class="navword">Tsamsiyu txur lu.</span>
			<br />
			<em>The warrior is strong.</em>
		</li>
	</ol>
	<p>
		That&#39;s pretty much what it looks like to describe something by saying what it <em>is</em> by using <span class="navword">lu</span>. But what if you wanted to describe it on the fly while talking about what it&#39;s doing or involved in? Check it out:
	</p>
	<h2 id="2">
		Describing with <span class="navword">a</span>
	</h2>
	<div id="endpostpreview"></div>
	<ol start="3" class="lindent">
		<li>
			<strong>
				<span class="blue-text">txantslusam</span>a <span class="red-text">eyktan</span> <span class="green-text">plltxe</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="red-text">eyktan</span> a<span class="blue-text">txantslusam</span> <span class="green-text">plltxe</span>.
			</strong>
			<br />
			<em>
				<span class="red-text">The</span> <span class="blue-text">wise</span> <span class="red-text">leader</span> <span class="green-text">speaks</span>.
			</em>
		</li>
		<li>
			<strong>
				<span class="blue-text">txur</span>a <span class="red-text">tsamsiyu</span><span class="green-text"> wem</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="red-text">tsamsiyu</span> a<span class="blue-text">txur</span> <span class="green-text">wem</span>.
			</strong>
			<br />
			<em>
				<span class="red-text">The</span> <span class="blue-text">strong</span> <span class="red-text">warrior</span> <span class="green-text">fights</span>.
			</em>
		</li>
		<li>
			<strong>
				<span class="orange-text">nga</span> <span class="green-text">lu</span> <span class="red-text">taronyu</span> a<span class="blue-text">sìltsan</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="orange-text">nga</span> <span class="green-text">lu</span> <span class="blue-text">sìltsan</span>a <span class="red-text">taronyu</span>.
			</strong>
			<br />
			<em>
				<span class="orange-text">You</span> <span class="green-text">are</span> <span class="red-text">a</span> <span class="blue-text">good</span> <span class="red-text">hunter</span>.
			</em>
		</li>
	</ol>
	<p>
		You probably notice that when <span class="blue-text">describing</span> the <span class="red-text">stuff</span> on the fly while talking about <span class="green-text">what it&#39;s doing</span>, there there is an -<strong>a</strong>- attached onto all the <span class="blue-text">blue words</span> that describe the <span class="red-text">red words</span>. You may also notice that the <strong>a</strong> is always attached onto the side of the <span class="blue-text">blue word</span> where the <span class="red-text">red word</span> is, and that the <span class="blue-text">blue word</span> is always <em>exactly next to</em> the <span class="red-text">red word</span>. This is because of rule. Notice the pattern and follow it. <img src="img/smiley.gif"/>
	</p>
	<p>
		Above, we talked about describing a <span class="red-text">person, place, or thing</span> with a single <span class="blue-text">describing word</span>. Now, I&#39;m going to show you how you can do exactly the same thing as above, but instead of describing it with just a <em>single word</em>, describe it with a <em>whole phrase of words</em>. Check it out:
	</p>
	<ol start="6" class="lindent">
		<li>
			<strong>
				<span class="blue-text">lrrtok si pxìm</span> a <span class="red-text">tuté</span> <span class="orange-text">oeru</span> <span class="green-text">sunu</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="green-text">sunu</span> <span class="orange-text">oeru</span> <span class="red-text">tuté</span> a <span class="blue-text">lrrtok si pxìm</span>.
			</strong>
			<br />
			<em>
				<span class="red-text">The</span> <span class="blue-text">smiles often</span> <span class="red-text">woman</span> <span class="green-text">is pleasing/likeable</span> <span class="orange-text">to me</span>.
			</em>
			<br />
			--&gt;
			<em>
				<span class="orange-text">I</span> <span class="green-text">like</span> <span class="red-text">the woman</span> who <span class="blue-text">smiles often</span>.
			</em>
		</li>
		<li>
			<strong>
				<span class="blue-text">fpe&#39; ayngal oer </span>a <span class="red-text">&#39;upxareti</span> <span class="green-text">stawm</span> <span class="purple-text">oel</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="purple-text">oel</span> <span class="green-text">stawm</span> <span class="red-text"> &#39;upxaret</span> a<span class="blue-text"> ayngal fpe&#39; oer</span>.
			</strong>
			<br />
			<em>
				<span class="purple-text">I</span> <span class="green-text">hear</span> <span class="red-text">the message</span> <span class="blue-text">you send to me</span>.
			</em>
		</li>
	</ol>
	<p>
		In this case, there&#39;s a <span class="purple-text">purple word</span>. The <span class="purple-text">purple word</span> is the one doing the <span class="green-text">action in green</span> to the <span class="red-text">red word</span>. "<span class="purple-text">I</span> <span class="green-text">hear</span> <span class="red-text">the message</span>" is the main skeleton of that sentence.
	</p>
	<ol start="8" class="lindent">
		<li>
			<strong>
				<span class="blue-text">ke yom tsnganit</span> a <span class="red-text">tute</span> <span class="green-text">lu</span> <span class="orange-text">po</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="orange-text">po</span> <span class="green-text">lu</span> <span class="red-text">tute</span> a<span class="blue-text"> ke yom tsnganit</span>.
			</strong>
			&#160;&#160; <u>
				<em>or</em>
			</u> &#160;&#160;
			<strong>
				<span class="orange-text">po</span> <span class="green-text">lu</span> <span class="blue-text">ke yom tsnganit </span>a <span class="red-text">tute</span>.
			</strong>
			<br />
			<em>
				<span class="orange-text">He</span>
				<span class="green-text">is</span>
				<span class="red-text">a</span>
				<span class="blue-text">doesn&#39;t eat meat</span>
				<span class="red-text">person</span>
			</em>
			<br />
			--&gt;
			<em>
				<span class="orange-text">He</span> <span class="green-text">is</span> <span class="red-text">a</span> <span class="blue-text">vege</span><span class="red-text">tarian</span>.
			</em>
		</li>
	</ol>
	<p>
		So in all these above cases, the <span class="blue-text">thing in blue</span> is next to and describing the <span class="red-text">thing in red</span>, and there is an <strong>a</strong> between them pointing from the <span class="blue-text">blue thing</span> to the <span class="red-text">red</span>.
	</p>
