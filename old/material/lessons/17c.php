	<p>
		<span class="navword">Kaltxì!</span>
		<!-- <img src="img/smiley.gif"/> --> Long time no post. This time I&#39;d like to talk about how to say someone does something to her/his self.
	</p>
	<h2>vtr. and äp</h2>
	<p>
		In English, we have special pronoun words like <span class="red-text">myself</span>, <span class="red-text">yourself</span>, <span class="red-text">himself</span>, <span class="red-text">herself</span>, <span class="red-text">ourselves</span> to get this idea across. For example:
	</p>
	<blockquote>
		<em>
			I <span class="cyan-text">see</span> <span class="red-text">myself</span>.
		</em>
	</blockquote>
	<p>
		In Na&#39;vi, we add &lt;<span class="red-text">äp</span>&gt; inside the <span class="cyan-text">verb</span>. The above example sentence would work like this:
	</p>
	<div class="endpostpreview"></div>
	<blockquote>
		<strong>
			<span style="font-size:1.1em">
				Oe <span class="cyan-text">
					ts<span class="red-text">äp</span>e&#39;a
				</span>.
			</span>
		</strong>
	</blockquote>
	<p>We do this instead of repeating the same person again:</p>
	<blockquote>
		<span class="orange-text">*</span>
		<strong>
			<span class="navword">Oel tse&#39;a oeti</span>
		</strong>
		<br />
		<span class="orange-text">*</span>
		<strong>
			<span class="navword">Ayngal tse&#39;a ayngati</span>
		</strong>
	</blockquote>
	<p>
		Did you notice that there is no marking on <strong>oe</strong> in that first example? Here&#39;s why. We can only even use &lt;<span class="red-text">äp</span>&gt; in verbs marked <em>vtr.</em> in the dictionary. And when we use &lt;<span class="red-text">äp</span>&gt;, it changes the <em>vtr.</em> into a <em>vin.</em> It then has the same structure as any old <em>vin.</em> like <strong>hahaw</strong> (<em>sleep</em>) or <strong>kä</strong> (<em>go</em>):
	</p>
	<blockquote>
		<strong>
			<span class="navword">Oe tsäpe&#39;a.</span>
		</strong> (I see myself.)<br />
		<strong>
			<span class="navword">Nga tìsraw säpeyki.</span>
		</strong> (You hurt yourself / cause yourself to hurt)<br />
		<strong>
			<span class="navword">Oe hahaw.</span>
		</strong> (I sleep.)<br />
		<strong>
			<span class="navword">Nga kä.</span>
		</strong> (You go.)<br />
	</blockquote>
	<p>
		Hey, check it out! That second example here has both &lt;<span class="red-text">äp</span>&gt; AND &lt;<span class="gold-text">eyk</span>&gt; in it. (Remember &lt;<span class="gold-text">eyk</span>&gt; from the previous lesson?) Both of these go first in the first raised dot of the dictionary entry of the verb.
	</p>
	<h2>vin. and sno</h2>
	<p>
		But what about actions which are <em>not</em> marked <em>vtr.</em>? A popular example is this: I know how to say "she loves her", which is <u>talking about two different people</u>:
	</p>
	<blockquote>
		<strong>
			Po <span class="cyan-text">yawne lu</span> <span class="green-text">poru</span>
		</strong> (she beloved is to her)
	</blockquote>
	<p>But how do I say "He loves himself"? Basically, like this:</p>
	<blockquote>
		<strong>
			Po <span class="cyan-text">yawne lu</span> <span class="red-text">snoru</span>
		</strong> (he beloved is to self)
	</blockquote>
	<p>
		<strong>
			<span class="red-text">sno</span>
		</strong> is just a special pronoun word that means <span class="red-text">(his/her-)self</span>. Use it when you need to say he or she is doing something to self, but the action is <em>not vtr.</em>
	</p>
	<h2>Me, myself, and vin. And you and them too.</h2>
	<p>
		What about if I want to do a <em>vin.</em> to myself or we to ourselves? Or even, if you wanted to do something to yourself, or they wanted to do something to themselves? Then, I believe that the same word would just get repeated again:
	</p>
	<blockquote>
		<span class="navword">Oe pamrel si oeru.</span> (I write to me)<br />
		<span class="navword">Ayoeng tìng nari awngaru.</span> (we look at us)<br />
		<span class="navword">Nga fyawìntxu ngaru.</span> (You guide you)<br />
		<span class="navword">Ayfo irayo si foru.</span> (They thank them)<br />
	</blockquote>
