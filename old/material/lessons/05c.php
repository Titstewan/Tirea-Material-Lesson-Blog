	<p>
		<span class="navword">Kaltxì nìmun</span>! This time we talk about how to ask and answer questions. Pretty simple stuff. Let&#39;s dive right in with some quick vocab. <img src="img/smiley.gif"/>
	</p>
	<h2 id="1">Question Words</h2>
	<p>
		The question words in Na&#39;vi <em>come from</em> the following words:
	</p>
	<ul class="nobullets">
		<li>
			<span class="navword">
				<u>tu</u>te
			</span>
			<em>person</em>
		</li>
		<li>
			<span class="navword">&#39;u</span>
			<em>thing</em>
		</li>
		<li>
			<span class="navword">tseng</span>
			<em>place</em>
		</li>
		<li>
			<span class="navword">lun</span>
			<em>reason</em>
		</li>
		<li>
			<span class="navword">krr</span>
			<em>time</em>
		</li>
		<li>
			<span class="navword">
				<u>fya</u>&#39;o
			</span>
			<em>way</em>
		</li>
	</ul>
	<p>
		To form question words like <em>who</em>, <em>what</em>, <em>where</em>, etc., You add a <span class="navword">pe</span>+ (<em>what/which</em>) onto the word. It can go either onto the beginning or onto the end. <span class="navword">Fya&#39;o</span> shortens to <span class="navword">fya</span> in this case. <span class="navword">Pefya / Fyape</span> is by far more common than <span class="navword">fya&#39;ope</span> or <span class="navword">pefya&#39;o</span>. <img src="img/wink.gif"/>
		And there&#39;s the + sign again. Remember a <a href="03.php">few lessons ago</a>, this means that <span class="navword">pe</span> when used on the beginning of a word, may cause the word&#39;s first letter to change.
	</p>
	<p>The question words in Na&#39;vi:</p>
	<ul class="nobullets">
		<li>
			<span class="navword">
				pe<u>su</u>
			</span> / <span class="navword">
				<u>tu</u>pe
			</span> <em>who</em>
		</li>
		<li>
			<span class="navword">
				pe<u>u</u>
			</span> / <span class="navword">
				<u>&#39;u</u>pe
			</span> <em>what</em>
		</li>
		<li>
			<span class="navword">
				pe<u>seng</u>
			</span> / <span class="navword">
				<u>tseng</u>pe
			</span> <em>where</em>
		</li>
		<li>
			<span class="navword">
				pe<u>lun</u>
			</span> / <span class="navword">
				<u>lum</u>pe
			</span> <em>why</em>
		</li>
		<li>
			<span class="navword">
				pe<u>hrr</u>
			</span> / <span class="navword">
				<u>krr</u>pe
			</span> <em>when</em>
		</li>
		<li>
			<span class="navword">
				pe<u>fya</u>
			</span> / <span class="navword">
				<u>fya</u>pe
			</span> <em>how</em>
		</li>
	</ul>
	<p>
		Notice that when you use <span class="navword">pe</span>+ on the end of the word, the letter changing bit is avoided. This is handy for people who often forget to change the first letter.
	</p>
	<p>Using these words is simple, and a lot like English. The only difference is that the word doesn&#39;t have a set place in the sentence because Na&#39;vi word order is flexible. So here are some examples of using some of these words:</p>
	<p>
		<u>Examples</u>:
	</p>
	<ol class="lindent">
		<li>
			<span class="navword">Nga tupe lu?</span>
			<br />
			<em>Who are you?</em>
		</li>
		<li>
			<span class="navword">Fìswiräti ngal pelun molunge fìtseng?</span>
			<br />
			<em>Why did you bring this creature here?</em>
		</li>
		<li>
			<span class="navword">Nga za&#39;u ftu peseng?</span>
			<br />
			<em>Where do you come from? / Where are you from?</em>
		</li>
	</ol>
	<p>
		It&#39;s pretty standard. (more on words like <span class="navword">fìswiräti</span> and <span class="navword">ftu</span> later)
	</p>
	<p>
		It is also possible to stick <span class="navword">pe</span>+ onto <u>any</u> noun. That is, <u>any</u> word which is a person, place, or thing.
	</p>
	<p>
		<u>Examples</u>:
	</p>
	<ol start="4" class="lindent">
		<li>
			<span class="navword">Ngaru sunu peioang frato?</span>
			<br />
			<em>Which beast do you like the most?</em>
			<br />
		</li>
		<li>
			<span class="navword">Tsamsiyupe lu lom?</span>
			<br />
			<em>Which warrior is missing?</em>
			<br />
		</li>
	</ol>
	<h2 id="2">Yes/No Questions with Srak(e)</h2>
	<p>
		All the above questions require a specific answer which is either the name of a person or place, or a time or a reason, etc. There is another very common type of question to ask: The yes/no question. This is super easy. All you do is add <span class="navword">srak</span> (aka <span class="navword">srake</span>) to the sentence, either first or last.
	</p>
	<p>
		<u>Examples</u>:
	</p>
	<ol start="6" class="lindent">
		<li>
			<span class="navword">Ngaru lu fpom.</span>
			<br />
			<em>you have peace.</em>
			<br />
			<span class="navword">Ngaru lu fpom srak?</span>
			<br />
			<em>Do you have peace?</em>
		</li>
		<li>
			<span class="navword">Yom ngal teylut.</span>
			<br />
			<em>You eat beetle larvae.</em>
			<br />
			<span class="navword">Srake yom ngal teylut?</span>
			<br />
			<em>Do you eat beetle larvae?</em>
		</li>
	</ol>
	<p>
		The answer to a <span class="navword">srake</span>/<span class="navword">srak</span> question is either <span class="navword">srane</span> (<em>yes</em>) <u>or</u> <span class="navword">kehe</span> (<em>no</em>). It&#39;s also possible, like in English, to describe things further than just a yes or no answer after you&#39;ve answered yes or no.
	</p>
