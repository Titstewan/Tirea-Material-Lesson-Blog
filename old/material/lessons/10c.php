	<h2 id="1">Srak/Srake, Srane and Kehe</h2>
	<p>
		So you may know by now the words <span class="navword">srane</span> (<em>yes</em>) and <span class="navword">kehe</span> (<em>no</em>).<br />
		Those are simple enough. They are acceptable answers to a question that has the word <span class="navword">srak</span> or <span class="navword">srake</span> in it.
	</p>
	<p>
		<u>For example:</u>
	</p>
	<blockquote>
		Q : &#160;<span class="navword">Nga &#39;efu ohakx srak?</span> (<em>Are you hungry?</em>)<br />
		A1: <span class="navword">Srane, nìtxan</span> (<em>Yes, very</em>)<br />
		A2: <span class="navword">Kehe, li yolom.</span> (<em>No, I already ate.</em>)<br />
	</blockquote>
	<h2 id="2">Ke - Not</h2>
	<p>
		In Na&#39;vi, in order to say someone does not do something or is not doing something, use the word <span class="navword">ke</span>. It must always go immediately before the verb.
	</p>
	<p>
		<u>For example:</u>
	</p>
	<blockquote>
		<span class="navword">Poe ke plltxe.</span> (<em>She doesn&#39;t speak.</em>)<br />
		<span class="navword">Ke tayaron oel yerikit.</span> (<em>I will not hunt a hexapede</em>)<br />
		<span class="navword">Kawtu ke plltxe nì&#39;Ìnglìsì.</span> (<em>No one speaks English.</em>)<br />
	</blockquote>
	<h2 id="3">Nothin&#39; Ain&#39;t True</h2>
	<p>
		Notice that in that last example, we still used <span class="navword">ke</span> even though <span class="navword">kawtu</span> means <em>nobody</em>. This is because there is a rule about this: Whenever using words like nobody, nowhere, or nothing, you still <strong>must</strong> use <span class="navword">ke</span> in front of the verb. It&#39;s like they make a pair.
	</p>
	<p>
		<u>More examples:</u>
	</p>
	<blockquote>
		<span class="navword">ke&#39;u ke lu ngay</span> (<em>Nothing is true.</em>)<br />
		<span class="navword">Oe kekem ke si.</span> (<em>I&#39;m doing nothing.</em>)<br />
		<span class="navword">Kawkrr tsatskxe ke zayup.</span> (<em>That rock will never fall.</em>)<br />
		<span class="navword">Fo ke lu &#39;ewan kaw&#39;it.</span> (<em>They are not young at all.</em>)<br />
	</blockquote>
	<h2 id="4">Not Every</h2>
	<p>
		If you want to say <em>not every</em>... then it&#39;s as easy as putting ke in front of the word that has <span class="navword">fra-</span> (<em>every-</em>). Don&#39;t forget to still put <span class="navword">ke</span> in front of the verb. <img src="img/wink.gif"/>
	</p>
	<p>
		<u>For example:</u>
	</p>
	<blockquote>
		<span class="navword">Ke frapo ke tslolam.</span> (<em>Not everyone understood.</em>)<br />
		<span class="navword">ke fratutan ke taron.</span> (<em>Not every man hunts.</em>)<br />
	</blockquote>
	<h2 id="5">I have none / I don&#39;t have any</h2>
	<p>
		Use <span class="navword">kea</span> (<em>no</em>) in front of nouns to say stuff like <em>He has no sisters.</em> instead of <em>He doesn&#39;t have a sister.</em>
	</p>
	<p>
		<u>For example:</u>
	</p>
	<blockquote>
		<span class="navword">Ke lu poru kea smuke.</span> (<em>He has no sisters</em>)<br />
		<span class="navword">Vay set ke pamähängem kea tì&#39;eyng.</span> (<em>Up to now, no answer arrived.</em>)<br />
	</blockquote>
	<h2 id="6">Don&#39;t Do That</h2>
	<p>
		When giving commands, do not use <span class="navword">ke</span>. <span class="navword">ke</span> is for making a statement into the opposite. To make a command into a <em>Do not</em>... command, you have to use <span class="navword">rä&#39;ä</span> right before the verb instead of <span class="navword">ke</span>. For super emphasis on the do NOT, you are allowed to put <span class="navword">rä&#39;ä</span> right after the verb.
	</p>
	<p>
		<u>For example:</u>
	</p>
	<blockquote>
		<span class="navword">rä’ä hahaw.</span> (<em>don’t sleep</em>)<br />
		<span class="navword">oeti ’ampi rä’ä, ma skxawng!</span> (<em>don’t touch me, you moron!</em>)<br />
	</blockquote>
