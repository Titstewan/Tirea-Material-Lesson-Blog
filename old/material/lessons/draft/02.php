  <h1 id="1">Asking the question</h1>

  <p>You may remember from the film that time Neytiri asks Tsu'tey what he is
    doing as he tries to capture Jake. Here's the line:
  </p>

  <p><span class="navword">Ma Tsu'<u>tey</u>, <u>kem</u>pe si nga?</span><br>
    <em>Tsu'tey, what are you doing?</em>
  </p>

  <p><span class="navword">ma</span> as you recall from the previous lesson, is
    a word used when addressing someone directly by name or title.<br>
    <span class="navword"><u>kem</u>pe</span> is the question word <em>what [action]?</em><br>
    <span class="navword">si</span> is a special kind of verb that means <em>do</em>.
    It's nearly always paired up with some word that goes just before it.
    All of these "<span class="navword">__ si</span>" verbs are listed in the dictionary.<br>
    <span class="navword">nga</span> is the most basic form of <em>you</em>
  </p>

  <p>There are other word orders and versions of this question, but the above
    remains most popular. Another popular version is like this:
  </p>

  <p><span class="navword">Nga pe<u>hem</u> si?</span><br>
    <em>What are you doing?</em>
  </p>

  <p>As you see, <span class="navword"><u>kem</u>pe</span> and
    <span class="navword">pe<u>hem</u></span> are two versions of the same word.
  </p>

  <h1 id="2">Answering the question</h1>

  <p>The way to do this is easy: just say what you're doing. Let's learn some words
    to help with this.
  </p>

  <h2>Actions</h2>
  <ul>
    <li><span class="navword">yom</span> (<em>eat</em>)</li>
    <li><span class="navword">näk</span> (<em>drink</em>)</li>
    <li><span class="navword"><u>ta</u>ron</span> (<em>hunt</em>)</li>
    <li><span class="navword"><u>star</u>sìm</span> (<em>gather</em>)</li>
    <li><span class="navword">pll<u>txe</u></span> (<em>speak</em>)</li>
    <li><span class="navword">u<u>van</u> si</span> (<em>play a game</em>)</li>
    <li><span class="navword">weyn</span> (<em>draw</em>)</li>
    <li><span class="navword"><u>ha</u>haw</span> (<em>sleep</em>)</li>
    <li><span class="navword">fti<u>a</u></span> (<em>study</em>)</li>
    <li><span class="navword"><u>yu</u>ne</span> (<em>listen to</em>)</li>
  </ul>

  <h2>Items</h2>
  <ul>
    <li><span class="navword">pay</span> (<em>water</em>)</li>
    <li><span class="navword">vey</span> (<em>food derived from animals</em>)</li>
    <li><span class="navword">fkxen</span> (<em>food derived from plants, vegetable</em>)</li>
    <li><span class="navword"><u>ma</u>uti</span> (<em>fruit</em>)</li>
    <li><span class="navword"><u>tey</u>lu</span> (<em>beetle larva</em>)</li>
    <li><span class="navword"><u>ye</u>rik</span> (<em>hexapede</em>)</li>
    <li><span class="navword"><u>ta</u>lioang</span> (<em>sturmbeest</em>)</li>
    <li><span class="navword"><u>pa</u>suk</span> (<em>berry</em>)</li>
    <li><span class="navword">rel</span> (<em>picture</em>)</li>
    <li><span class="navword"><u>lì'</u>fya</span> (<em>language</em>)</li>
    <li><span class="navword"><u>rey</u>fya</span> (<em>culture</em>)</li>
    <li><span class="navword"><u>pam</u>tseo</span> (<em>music</em>)</li>
  </ul>

  <p>Let's put some of these together into some basic sentences. We'll need some
    form of <em>I</em> such as <span class="navword"><u>o</u>e</span> or <span class="navword">o<u>e</u>l</span>.
    Feel free to play around with these, following the way it's done in the examples:
  </p>

  <p><span class="navword"><u>O</u>e <u>ha</u>haw.</span><br>
    <em>I sleep.</em>
  </p>

  <p>This type of sentence is the simplest and easiest possible sentence in the
    language, due to the action being something that only involves one being/person.
    In situations like these, all you need is two words straight out of the
    dictionary without any changes. Here's another one:
  </p>

  <p><span class="navword">U<u>van</u> si <u>o</u>e.</span><br>
    <em>I play a game.</em>
  </p>

  <p>The word order is flexible, so {<span class="navword">u<u>van</u> si</span>} and {<span class="navword"><u>o</u>e</span>} can come in both orders
    and correctly mean the same thing.
  </p>

  <p><span class="navword">O<u>e</u>l <u>tey</u>luti yom.</span><br>
    <em>I eat teylu.</em><br>
    Literally: I teylu eat.
  </p>

  <p>This time, the action is something that involves two parties: The one eating
    and what's being eaten. Because word order is flexible, we need some new way
    to keep track of what's eating what, since word order doesn't do that for us
    like in English. In Na'vi, whatever has the <span class="navword">-l / -ìl</span>
    ending does the action and whatever has the <span class="navword">-ti / -it / -t</span>
    ending gets the action applied to it or done to it. Here are some more examples:
  </p>

  <p><span class="navword"><u>Yu</u>ne o<u>e</u>l <u>pam</u>tseot.</span><br>
    <em>I listen to music.</em><br>
    Literally: Listen-to I music.
  </p>

  <p><span class="navword">O<U>el</u> fti<u>a</u> <u>lì'</u>fyati.</span><br>
    <em>I study [a/the] language.</em>
  </p>

  <p><span class="navword">O<u>e</u>l näk <u>pay</u>ti</span><br>
    <em>I drink water.</em>
  </p>

  <p><span class="navword"><u>O</u>e pll<u>txe</u>.</span><br>
    <em>I speak.</em>
  </p>

  <p><span class="navword">O<u>e</u>l <u>rel</u>it weyn.</span><br>
    <em>I draw picture.</em>
  </p>

  <p>And if you happen to be doing nothing, then you can simply say,
    "<span class="navword">[oe] <u>ke</u>kem <u>ke</u> si</span>." That's a pretty popular
    answer to <span class="navword"><u>kem</u>pe si nga?</span>
  </p>

