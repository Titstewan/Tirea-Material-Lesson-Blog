	<p>
		<span class="navword">Kaltxì</span>.
			As the title says, this time I&#39;m gonna talk about how colours work. It&#39;s a bit late, but it was requested. This post is basically a summary of <a href="http://naviteri.org/2010/08/mipa-ayopin-mipa-ayliu-new-colors-new-words/">the one Pawl made on his blog</a>.
	</p>
	<p>So, I think the best way to learn colours is to look at actual colours labeled by the Na&#39;vi words. Check out these (from Karyu Pawl&#39;s Blog):</p>
