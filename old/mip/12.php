      <h1>Lesson Twelve: -ing in Na&#39;vi</h1>
      <a id="prevpost" href="11.php">&lt;- Previous</a>
      <a id="nextpost" href="13.php">Next -&gt;</a>
      <br />
      <p>
        <span class="navi">Kaltxì</span> nìmun! This time I wanna talk about the three different ways to say -ing in Na&#39;vi. <img src="img/smiley.gif"/>
      </p>
      <p>In English, we use the same -ing ending in at least three different ways. In Na&#39;vi, all three of those translate to three different ways instead of using the same one.</p>
      <p>
        <u>In English</u>, the three main uses for -ing are as in the following examples:
      </p>
      <ol class="lindent">
        <li>
          <em>
            She <span class="red">is</span> hunt<span class="red">ing</span> a hexapede
          </em>.
        </li>
        <li>
          <em>
            A hunt<span class="lime">ing</span> thanator is dangerous.
          </em>
        </li>
        <li>
          <em>
            Hunt<span class="gdocblue">ing</span> is fun.
          </em>
        </li>
      </ol>
      <p>
        <u>In Na&#39;vi</u>, these same three are:
      </p>
      <ol class="lindent">
        <li>
          <strong>
            Pol t<span class="red">er</span>aron yerikit.
          </strong>
        </li>
        <li>
          <strong>
            T<span class="lime">us</span>aron<span class="yellow">a</span> palulukan lu lehrrap.
          </strong>
        </li>
        <li>
          <strong>
            <span class="gdocblue">Tì</span>t<span class="lime">us</span>aron lu &#39;o&#39;.
          </strong>
        </li>
      </ol>
      <p>
        In 1, we use the position 1 infix <span class="red">er</span> to show that "hunt" is an ongoing action, "hunting". Note that <span class="red">er</span> already has the meaning of "<span class="red">am/is/are</span> do<span class="red">ing</span>" built right in. So it&#39;s redundant and incorrect to say stuff like *Po lu teraron.<br  />
        <br  />
        In 2, the infix <span class="lime">us</span> is used. <span class="lime">us</span> is another position 1 infix, but instead of making a verb ongoing, it turns it into an adjective! T<span class="lime">us</span>aron<span class="yellow">a</span> palulukan, "a hunt<span class="lime">ing</span> thanator" -- "hunt<span class="lime">ing</span>" is an <em>adjective</em> describing the thanator, rather than saying "the thanator <span class="red">is</span> hunt<span class="red">ing</span>", which would use <span class="red">er</span>. Note that because the verb has been turned into an adjective, you must use -<span class="ygold">a</span>- with it. Also, <span class="lime">us</span> adjectives cannot be used with lu, as in *Po lu tusaron. You must say that as Po t<span class="red">er</span>aron, "he <span class="red">is</span> hunt<span class="red">ing</span>."<br  />
        <br  />
        In 3, we have taken the <span class="lime">us</span> form, and added the <span class="gdocblue">tì</span>- prefix to make it into a noun. You can add <span class="gdocblue">tì</span>- and <span class="lime">us</span> to any verb to make it mean "(<span class="gdocblue">the activity</span> of) verb-ing". <span class="gdocblue">Tì</span>t<span class="lime">us</span>aron lu &#39;o&#39;. (<span class="gdocblue">the activity</span> of) hunting is fun.
      </p>
      <p>Hope this clears things up.</p>
