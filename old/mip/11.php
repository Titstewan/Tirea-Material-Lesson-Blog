      <p>
        This post is about the Na&#39;vi word <span class="navi">pum</span>. It&#39;s a word that I constantly see misused. I hope that this blog post will clear up what <span class="navi">pum</span> actually means and how to use it properly.
      </p>
      <p>
        According to the dictionary, <span class="navi">pum</span> means <em>one</em>. One as in: this one, that one, the good one, the bad one, my one(mine), your one(yours).. I guess examples would be the best and easiest way of explaining. Here are nearly all the examples of <span class="navi">pum</span> we have from Karyu Pawl:
      </p>
      <p>
        <u>Examples:</u>
      </p>
      <ol class="lindent">
        <li>
          <span class="navi">Kelku ngeyä lu tsawl; pum oeyä lu hì’i.</span>
          <br  />
            <em>Your house is large; mine(my one) is small.</em>
        </li>
        <li>
          <span class="navi">Just a relatively short post before the month ends... slä nìsìlpey pum a nga’ aylì’fyavit lesar.</span>
          <br  />
            <em>Just a relatively short post before the month ends... but hopefully one which contains useful expressions.</em>
        </li>
        <li>
          <span class="navi">Hufwa mefo leru muntxatu txankrr, mi lu munsnar hona tìflrr a na pum meyawnetuä amip nìwotx.</span>
          <br  />
            <em>Although they&#39;ve been mates for a long time, the couple still has cute gentleness like one of two totally new lovers.</em>
        </li>
        <li>
          <span class="navi">Tsatuté lu lora pum alor.</span>
          <br  />
            <em>That woman is an extremely beautiful one.</em>
        </li>
        <li>
          <span class="navi">Pol zolet oeyä säfpìlit pxel pum a tìngäzìkit ngop.</span>
          <br  />
            <em>He treated my idea as one—i.e., an idea—that creates a problem.</em>
        </li>
        <li>
          <span class="navi">Ta’leng prrnenä lu faoi, pum koaktuä ekxtxu.</span>
          <br  />
            <em>A baby’s skin is smooth, an old person’s is rough.</em>
        </li>
        <li>
          <span class="navi">Tìhawl lesngä’i lu tìkangkemvi skxawngä, slä pum alu fì’u yo’ nì’aw.</span>
          <br  />
            <em>The original plan was the work of an idiot, but this one is just perfect.</em>
        </li>
        <li>
          <span class="navi">Fìfkxen alu FÌ’u lu ftxìlor; tsafkxen (or: pum) alu TSA’u ngati tspang.</span>
          <br  />
            <em>THIS vegetable is delicious; THAT one will kill you.’</em>
        </li>
        <li>
          <span class="navi">Krro krro, flìa vul arusey to nutxa pum akerusey lu txur.</span>
          <br  />
            <em>A thin living branch is sometimes stronger than a thick dead one.</em>
        </li>
        <li>
          <span class="navi">Ftue lu fwa taron ngonga ioangit to fwa taron pumit a lu walak sì win.</span>
          <br  />
            <em>It’s easier to hunt lethargic animals than to hunt perky, speedy ones.</em>
        </li>
        <li>
          <span class="navi">Pum ngeyä.</span>
          <br  />
            <em>Yours.</em> (That is, you’ve thanked me, but I really should be thanking you. The thanks are/should be yours)
        </li>
      </ol>
      <p>Here are some things I&#39;ve noticed after looking at all these official examples:</p>
      <ol class="lindent">
        <li>
          <span class="navi">pum</span> isn&#39;t used/translated as <em>it</em>, but nearly always <em>one</em>
        </li>
        <li>
          <span class="navi">pum</span> seems to always occur later on in the sentence
        </li>
        <li>
          <span class="navi">pum</span> is always involved in descriptions. It always has an adjective or phrase or something describing it.
        </li>
        <li>
          <span class="navi">pum</span> is never changed for number. So there is no *ayfum, *mefum, etc.
        </li>
      </ol>
