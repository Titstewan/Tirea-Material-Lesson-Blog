      <h2 id="2">Those Crazy "____ si" Words</h2>
      <p>
        Now. I&#39;ll bet most of you if not all have seen the Na&#39;vi word <strong>si</strong>. I know that the dictionary definiton is <em>crazy</em> and scary. It&#39;s actually simple. <img src="img/smiley.gif"/> Here&#39;s how it works:
      </p>
      <div id="endpostpreview"></div>
      <p>
        <u>Examples</u>:
      </p>
      <ol start="6" class="lindent">
        <li>
          <strong>
            <span class="orange">Nga</span><span class="turquoise">ru</span> <span class="green">irayo si</span> oe.
          </strong>
          <br />
          I <span class="green">do thanks</span> <span class="turquoise">to</span> <span class="orange">you</span>.<br />
          <em>I thank you.</em>
        </li>
        <li>
          <strong>
            <span class="orange">Oe</span><span class="turquoise">ru</span> <span class="green">srung si</span> nga.
          </strong>
          <br />
          You <span class="green">do assistance</span> <span class="turquoise">to</span> <span class="orange">me</span><br />
          <em>you help me.</em>
        </li>
        <li>
          <strong>
            Po <span class="orange">awnga</span><span class="turquoise">ru</span> <span class="green">kavuk si</span>.
          </strong>
          <br />
          He <span class="green">does betrayal</span> <span class="turquoise">to</span> <span class="orange">us</span>.<br />
          <em>He betrays us.</em>
        </li>
        <li>
          <strong>
            Tsa&#39;u <span class="purple">oeyä</span> <span class="orange">eltu</span><span class="turquoise">r</span> <span class="green">tìtxen si</span>.
          </strong>
          <br />
          That <span class="green">does awakening</span> <span class="turquoise">to</span> <span class="purple">my</span> <span class="orange">brain</span>.<br />
