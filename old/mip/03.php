      <p>
        <span class="navi">Kaltxì</span>! And welcome to Lesson 3 of Learn Na&#39;vi Grammar The Easy Way.
      </p>
      <p>This time, we talk about having something, and how to talk about more than one thing.</p>
      <h2 id="1">To Have</h2>
      <p>
        First, let&#39;s talk about having something. You have most likely noticed that a verb <i>to have</i> does not exist in Na&#39;vi. Different languages have different ways of saying things.
      </p>
      <p>
        <u>In English</u>, we say stuff like this:
      </p>
      <ul class="nobullets">
        <li>
          <i>
            <span class="blue">I</span> <span class="green">have</span> <span class="red">a crossbow</span>.
          </i>
        </li>
        <li>
          <i>
            <span class="blue">You</span> <span class="green">have</span> <span class="red">a bow</span>.
          </i>
        </li>
        <li>
          <i>
            <span class="blue">He</span> <span class="green">has</span> <span class="red">a spear</span>.
          </i>
        </li>
      </ul>
      <p>
        <u>In Na&#39;vi</u>, we say those this way:
      </p>
      <ul class="nobullets">
        <li>
          <strong>
            <span class="green">Lu</span> <span class="blue">oe</span><span class="green">ru</span> <span class="red">tskalep</span>.
          </strong>
        </li>
        <li>
          <strong>
            <span class="blue">Nga</span><span class="green">ru</span> <span class="green">lu</span> <span class="red">tsko</span>.
          </strong>
        </li>
        <li>
          <strong>
            <span class="green">Lu</span> <span class="blue">po</span><span class="green">ru</span> <span class="red">tukru</span>.
          </strong>
        </li>
      </ul>
      <p>
        Do you see the pattern? You actually say <i>
          <span class="red">A crossbow</span>
          <span class="green">is</span>
          <span class="blue">to me</span>
        </i>, <i>
          <span class="red">A bow</span>
          <span class="green">is</span>
          <span class="blue">to you</span>
        </i>, and <i>
          <span class="red">A spear</span>
          <span class="green">is</span>
          <span class="blue">to him</span>
        </i>. Remember, Na&#39;vi word order is flexible. I chose to write the second one differently. the words used in these examples are as follows:
      </p>
      <div id="endpostpreview"></div>
      <table>
        <tr>
          <td>
