      <p>Some of these na- colour things are so common, they are shortened:</p>
      <blockquote>
        <span class="navi">ta&#39;lengna ean</span> > <span class="navi">ta&#39;lengean</span><br />
          <span class="navi">rìkna ean</span> > <span class="navi">rìkean</span><br />
