      <p>All of these colour words are adjectives, not nouns. So you use them like this:</p>
      <blockquote>
        <span class="navi">Fìsyulang lu rim.</span> (<em>This flower is yellow.</em>)<br />
          <span class="navi">Fìsyulang arim lu hì&#39;i frato.</span> (<em>This yellow flower is the smallest of all.</em>)
      </blockquote>
      <p>
        If you want to use the noun, add <strong>-pin</strong> onto the end of the colour word, like this:
      </p>
      <blockquote>
        <span class="navi">Ke sunu oeru rimpin.</span> (<em>I don’t like the colour yellow.</em>)
      </blockquote>
      <p>
        If you want to be more specific, you compare the colour with something. We do this in English: <em>leaf green, sky blue, brick red, etc.</em> in Na&#39;vi, use <span class="navi">na</span> (<em>like,as</em>):
      </p>
      <blockquote>
        <span class="navi">Fìsyulang lu ean na ta’leng.</span> (<em>This flower is blue like skin.</em>)<br />
          -OR- <br />
            <span class="navi">Fìsyulang lu ta&#39;lengna ean.</span> (<em>This flower is blue like skin.</em>)<br />
      </blockquote>
      <p>
        To say <em>This skin-blue flower is very beautiful</em>, you have 4 choices:
      </p>
      <ol class="lindent">
        <li>
          <span class="navi">Fìsyulang aean-na-ta&#39;leng lor lu nìtxan.</span>
        </li>
        <li>
          <span class="navi">Fìsyulang ata&#39;lengna-ean lor lu nìtxan.</span>
        </li>
        <li>
          <span class="navi">Ean-na-ta&#39;lenga fìsyulang lor lu nìtxan.</span>
        </li>
        <li>
          <span class="navi">Ta&#39;lengna-eana fìsyulang lor lu nìtxan.</span>
          </li>
        </ol>
      <p>
        (There are actually more choices, if you move the <span class="navi">lor lu nìtxan</span> part around.)
      </p>
      <p>
        You are free to come up with these comparisons with <span class="navi">na</span> on your own, as long as there’s good reason to expect that you&#39;ll be understood.
      </p>
