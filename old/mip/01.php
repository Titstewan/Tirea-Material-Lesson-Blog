      <h1>Lesson One: Order, -l, and -t</h1>
      <a id="nextpost" href="02.php">Next -&gt;</a>
      <br />
        <p>
          <span class="navi">Kaltxì</span>! And welcome to Lesson 1 of Learn Na&#39;vi Grammar the Easy Way.
        </p>
        <p>Ok so to start, let&#39;s talk about how words fit together to make sentences.</p>
        <h2>Word Order</h2>
        <p>
          <u>In English</u>, there is an order which words have to go in order for the sentence makes sense:
        </p>
        <blockquote>
          <span class="blue">people</span> eat <span class="red">vegetables</span>.
        </blockquote>
        <p>
          It&#39;s clear here who is eating what because of the order. We always put <span class="blue">who is doing the eating</span> first, then <i>eat</i> and then <span class="red">what is being eaten</span>. Because of this word order telling us this information of who&#39;s eating who, we don&#39;t need to change the words themselves. we just put them in their place. If you mix up the words, the meaning completely changes:
        </p>
        <blockquote>
          <span class="red">vegetables</span> eat <span class="blue">people</span>.
        </blockquote>
        <p>This has the opposite meaning as above.</p>
        <p>
          <u>In Na&#39;vi</u>, there is more freedom. This means that the word order doesn&#39;t matter as much. Because of that, we need to change the words themselves ever so slightly sometimes in order to keep track of who is eating and what&#39;s being eaten. Therefore:
        </p>
        <blockquote>
          <strong>
            <span class="blue">aysutel</span> yom <span class="red">ayfkxenit</span>
          </strong>.
        </blockquote>
        <p>and</p>
        <blockquote>
          <strong>
            <span class="red">ayfkxenit</span> yom <span class="blue">aysutel</span>
          </strong>.
        </blockquote>
        <p>are equal in meaning. This also means that</p>
        <blockquote>
          <strong>
            <span class="blue">ayfkxenìl</span> yom <span class="red">aysuteti</span>
          </strong>.
        </blockquote>
        <p>and</p>
        <blockquote>
          <strong>
            <span class="red">aysuteti</span> yom <span class="blue">ayfkxenìl</span>
          </strong>.
        </blockquote>
        <p>are equal in meaning but these two senteces mean the opposite of all the four sentences above them.</p>
        <h2>Some Stuff</h2>
        <p>So you may have noticed that the Na&#39;vi words above are these:</p>
        <ul class="nobullets">
          <li>
            <span class="navi">aysutel</span>
          </li>
          <li>
            <span class="navi">aysuteti</span>
          </li>
          <li>
            <span class="navi">yom</span>
          </li>
          <li>
            <span class="navi">ayfkxenìl</span>
          </li>
          <li>
            <span class="navi">ayfkxenit</span>
          </li>
        </ul>
        <p>
          <strong>
            <span class="blue">aysutel</span>
          </strong> and <strong>
            <span class="red">aysutet</span>
          </strong> come from <span class="navi">aysute</span> (<i>people</i>) which is several <span class="navi">tute</span> (<i>person</i>).
        </p>
        <p>
          <span class="navi">yom</span> is <i>eat</i>
        </p>
        <p>
          <strong>
            <span class="blue">ayfkxenìl</span>
          </strong> and <strong>
            <span class="red">ayfkxenit</span>
          </strong> come from <span class="navi">ayfkxen</span> (<i>vegetables</i>) which is several <span class="navi">fkxen</span> (<i>vegetable</i>)
        </p>
        <p>
          As you may have noticed, these words have either <strong>
            <span class="blue">l</span>
          </strong>, <strong>
            <span class="blue">ìl</span>
          </strong>, <strong>
            <span class="red">it</span>
          </strong>, or <strong>
            <span class="red">ti</span>
          </strong> on the end. This is, as described above, how we keep track of who&#39;s eating what. We put stuff on the end.
        </p>
        <p>notice:</p>
        <ul class="nobullets">
          <li>All the words that have these endings are always either a Person, Place, or thing.</li>
          <li>
            <span class="navi">yom</span> is a word listed as type <i>vtr.</i> in the LearnNavi.org dictionary. ONLY words listed as <i>vtr.</i> (or sometimes <i>vtrm.</i>) can be used with these endings.
          </li>
        </ul>
        <p>
          <strong>
            <span class="blue">l</span>
          </strong> and <strong>
            <span class="blue">ìl</span>
          </strong> are the same. There are two of them so there is a choice to use which one flows the best. Only use <strong>
            <span class="blue">ìl</span>
          </strong> on words that end with consonants (like n, t, p, k, kx, ng for example.), and <strong>
            <span class="blue">l</span>
          </strong> on words that end with a vowel (like, a, ä, e, i, ì, o, u). These are put on the thing which is doing the action. Here, <span class="blue">the thing that is doing the eating</span>.
        </p>
        <p>
          <strong>
            <span class="red">ti</span>
          </strong> and <strong>
            <span class="red">it</span>
          </strong> are the same. Use <strong>
            <span class="red">it</span>
          </strong> on words that end with consonants, and <strong>
            <span class="red">ti</span>
          </strong> on words that end with a vowel. These are put on the one that is receiving the action. Here, <span class="red">the thing that is being eaten</span>.
        </p>
