      <h1>Lesson Four: Describing Stuff</h1>
      <a id="prevpost" href="03.php">&lt;- Previous</a>
      <a id="nextpost" href="05.php">Next -&gt;</a>
      <br />
      <p>
        <span class="navi">Kaltxì</span>! And welcome to Lesson 4 of Learn Na&#39;vi Grammar The Easy Way.
      </p>
      <p>This time we talk about describing stuff.</p>
      <h2 id="1">
        Describing with <span class="navi">lu</span>
      </h2>
      <p>
        In Na&#39;vi, We describe what something is with <span class="navi">lu</span>. In this case, it&#39;s like an "=" sign. Some examples:
      </p>
      <ol class="lindent">
        <li>
          <span class="navi">Eyktan lu txantslusam.</span>
          <br />
          <em>The leader is wise.</em>
        </li>
        <li>
          <span class="navi">Tsamsiyu txur lu.</span>
          <br />
          <em>The warrior is strong.</em>
        </li>
      </ol>
      <p>
        That&#39;s pretty much what it looks like to describe something by saying what it <em>is</em> by using <span class="navi">lu</span>. But what if you wanted to describe it on the fly while talking about what it&#39;s doing or involved in? Check it out:
      </p>
      <h2 id="2">
        Describing with <span class="navi">a</span>
      </h2>
      <div id="endpostpreview"></div>
      <ol start="3" class="lindent">
        <li>
          <strong>
            <span class="blue">txantslusam</span>a <span class="red">eyktan</span> <span class="green">plltxe</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="red">eyktan</span> a<span class="blue">txantslusam</span> <span class="green">plltxe</span>.
          </strong>
          <br />
          <em>
            <span class="red">The</span> <span class="blue">wise</span> <span class="red">leader</span> <span class="green">speaks</span>.
          </em>
        </li>
        <li>
          <strong>
            <span class="blue">txur</span>a <span class="red">tsamsiyu</span><span class="green"> wem</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="red">tsamsiyu</span> a<span class="blue">txur</span> <span class="green">wem</span>.
          </strong>
          <br />
          <em>
            <span class="red">The</span> <span class="blue">strong</span> <span class="red">warrior</span> <span class="green">fights</span>.
          </em>
        </li>
        <li>
          <strong>
            <span class="orange">nga</span> <span class="green">lu</span> <span class="red">taronyu</span> a<span class="blue">sìltsan</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="orange">nga</span> <span class="green">lu</span> <span class="blue">sìltsan</span>a <span class="red">taronyu</span>.
          </strong>
          <br />
          <em>
            <span class="orange">You</span> <span class="green">are</span> <span class="red">a</span> <span class="blue">good</span> <span class="red">hunter</span>.
          </em>
        </li>
      </ol>
      <p>
        You probably notice that when <span class="blue">describing</span> the <span class="red">stuff</span> on the fly while talking about <span class="green">what it&#39;s doing</span>, there there is an -<strong>a</strong>- attached onto all the <span class="blue">blue words</span> that describe the <span class="red">red words</span>. You may also notice that the <strong>a</strong> is always attached onto the side of the <span class="blue">blue word</span> where the <span class="red">red word</span> is, and that the <span class="blue">blue word</span> is always <em>exactly next to</em> the <span class="red">red word</span>. This is because of rule. Notice the pattern and follow it. <img src="img/smiley.gif"/>
      </p>
      <p>
        Above, we talked about describing a <span class="red">person, place, or thing</span> with a single <span class="blue">describing word</span>. Now, I&#39;m going to show you how you can do exactly the same thing as above, but instead of describing it with just a <em>single word</em>, describe it with a <em>whole phrase of words</em>. Check it out:
      </p>
      <ol start="6" class="lindent">
        <li>
          <strong>
            <span class="blue">lrrtok si pxìm</span> a <span class="red">tuté</span> <span class="orange">oeru</span> <span class="green">sunu</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="green">sunu</span> <span class="orange">oeru</span> <span class="red">tuté</span> a <span class="blue">lrrtok si pxìm</span>.
          </strong>
          <br />
          <em>
            <span class="red">The</span> <span class="blue">smiles often</span> <span class="red">woman</span> <span class="green">is pleasing/likeable</span> <span class="orange">to me</span>.
          </em>
          <br />
          --&gt;
          <em>
            <span class="orange">I</span> <span class="green">like</span> <span class="red">the woman</span> who <span class="blue">smiles often</span>.
          </em>
        </li>
        <li>
          <strong>
            <span class="blue">fpe&#39; ayngal oer </span>a <span class="red">&#39;upxareti</span> <span class="green">stawm</span> <span class="purple">oel</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="purple">oel</span> <span class="green">stawm</span> <span class="red"> &#39;upxaret</span> a<span class="blue"> ayngal fpe&#39; oer</span>.
          </strong>
          <br />
          <em>
            <span class="purple">I</span> <span class="green">hear</span> <span class="red">the message</span> <span class="blue">you send to me</span>.
          </em>
        </li>
      </ol>
      <p>
        In this case, there&#39;s a <span class="purple">purple word</span>. The <span class="purple">purple word</span> is the one doing the <span class="green">action in green</span> to the <span class="red">red word</span>. "<span class="purple">I</span> <span class="green">hear</span> <span class="red">the message</span>" is the main skeleton of that sentence.
      </p>
      <ol start="8" class="lindent">
        <li>
          <strong>
            <span class="blue">ke yom tsnganit</span> a <span class="red">tute</span> <span class="green">lu</span> <span class="orange">po</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="orange">po</span> <span class="green">lu</span> <span class="red">tute</span> a<span class="blue"> ke yom tsnganit</span>.
          </strong>
          &#160;&#160; <u>
            <em>or</em>
          </u> &#160;&#160;
          <strong>
            <span class="orange">po</span> <span class="green">lu</span> <span class="blue">ke yom tsnganit </span>a <span class="red">tute</span>.
          </strong>
          <br />
          <em>
            <span class="orange">He</span>
            <span class="green">is</span>
            <span class="red">a</span>
            <span class="blue">doesn&#39;t eat meat</span>
            <span class="red">person</span>
          </em>
          <br />
          --&gt;
          <em>
            <span class="orange">He</span> <span class="green">is</span> <span class="red">a</span> <span class="blue">vege</span><span class="red">tarian</span>.
          </em>
        </li>
      </ol>
      <p>
        So in all these above cases, the <span class="blue">thing in blue</span> is next to and describing the <span class="red">thing in red</span>, and there is an <strong>a</strong> between them pointing from the <span class="blue">blue thing</span> to the <span class="red">red</span>.
      </p>
